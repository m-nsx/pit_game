#!/bin/bash

# Termine tous les processus du jeu
pkill -f "timer.sh"
echo "Tous les processus ont été arrêtés !"

# Supprimer les fichiers et les dossiers du jeu
root_dir="."
find "$root_dir" -type f -name "archive_*" -exec rm -f {} \; > /dev/null 2>&1
for dir in */; do
    if [ "$dir" != "doc/" ]; then
        rm -rf "$dir"
    fi
done
rm -f code
rm -f .hint
rm -f time
rm -f key_file.enc
rm -f key_file
rm -f archive.tar.gz
rm -f defuse
echo "Tous les fichiers et dossiers ont été nettoyés !"

# Reset terminé
echo "Remise à zéro terminée !"

exit 0


































































